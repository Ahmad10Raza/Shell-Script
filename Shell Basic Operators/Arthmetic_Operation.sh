# Basics Arthmetic Operations

# Addition
a=10
b=20
echo "Addition of $a and $b is: $((a+b))"

# Subtraction
echo "Subtraction of $a and $b is: $((a-b))"

# Multiplication
echo "Multiplication of $a and $b is: $((a*b))"

# Division
echo "Division of $a and $b is: $((a/b))"

# Modulus
echo "Modulus of $a and $b is: $((a%b))"

# Exponentiation
echo "Exponentiation of $a and $b is: $((a**b))"

# Increment
echo "Increment of $a is: $((a++))"

# Decrement
echo "Decrement of $a is: $((a--))"

# Output:
# Addition of 10 and 20 is: 30
# Subtraction of 10 and 20 is: -10
# Multiplication of 10 and 20 is: 200
# Division of 10 and 20 is: 0
# Modulus of 10 and 20 is: 10
# Exponentiation of 10 and 20 is: 100000000000000000000
# Increment of 10 is: 10
# Decrement of 10 is: 10
